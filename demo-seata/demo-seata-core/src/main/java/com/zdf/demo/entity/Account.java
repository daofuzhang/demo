package com.zdf.demo.entity;

import java.math.BigDecimal;

public class Account {

	private int id;
	private BigDecimal money;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public BigDecimal getMoney() {
		return money;
	}
	public void setMoney(BigDecimal money) {
		this.money = money;
	}
	
	
}
